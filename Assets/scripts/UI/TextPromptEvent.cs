// ========================================================================
// 文件功能：文本提示事件枚举定义
// 定义了游戏中可触发的各类文本提示事件类型，用于 TextPromptManager 等系统
// 按事件显示对应的提示信息。该枚举可在其他脚本的 Inspector 面板中作为下拉选择项显示。
// ========================================================================

public enum TextPromptEvent
{
    // 场景开始时显示
    SceneStart,
    // 固定物体到视角时显示
    AnchorObject,
    // 将物体从视角取消固定时显示
    UnanchorObject,
    // 关卡完成时显示
    LevelComplete,
    // 玩家死亡时显示
    PlayerDeath,
    // 自定义事件1
    Custom1,
    // 自定义事件2
    Custom2,
    // 自定义事件3
    Custom3
}