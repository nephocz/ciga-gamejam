// ========================================================================
// 文件功能：音效类型枚举定义
// 定义了游戏中所有可用的音效类型，供 SFXManager 等系统播放相应音频。
// 该枚举可在其他脚本的 Inspector 面板中作为下拉选择项显示。
// ========================================================================

public enum SFXType
{
    // 场景过渡音效
    SceneTransition,
    // 固定物体到视角时的音效
    AnchorObject,
    // 将物体从视角取消固定时的音效
    UnanchorObject,
    // 按钮点击音效
    ButtonClick,
    // 关卡完成音效
    LevelComplete,
    // 玩家移动循环音效
    PlayerMove
}